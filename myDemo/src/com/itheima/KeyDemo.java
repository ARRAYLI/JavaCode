package com.itheima;

/*
 * 内容辅助键：alt + / 
 * 		A:main方法
 * 			main,然后alt+/,回车
 * 		B:输出语句
 * 			sys,然后 alt+/，回车
 * 
 * 快捷键：
 * 		A:注释
 * 			单行   		选中内容  ctl+/,然后再来一次取消注释
 * 			多行   	 	选中内容 ctl+shift+/，取消注释 ctrl+shift+\
 * 		B:格式化
 * 			ctrl+shift+f
 * 			右I----Source----Format
 * 
 */
public class KeyDemo {
	public static void main(String[] args) {
		System.err.println("HelloWorld!");
		System.err.println("HelloWorld!");
		System.err.println("HelloWorld!");
		System.err.println("HelloWorld!");
	}
}
