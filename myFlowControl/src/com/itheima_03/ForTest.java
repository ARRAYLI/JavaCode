package com.itheima_03;

public class ForTest {
	public static void main(String[] args) {
		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
		System.out.println(4);
		System.out.println(5);
		System.out.println("---------------------");

		for (int x = 1; x <= 5; x++) {
			System.out.println(x);
		}
		System.out.println("---------------------");
		for (int x = 5; x > 0; x--) {
			System.out.println(x);
		}
	}
}
